"""
    –ù–∞–ø–∏—à–∏—Ç–µ —Ñ—É–Ω–∫—Ü–∏—é solve(a, b, c), –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ —Ç—Ä–∏ —Ü–µ–ª—ã—Ö —á–∏—Å–ª–∞ 
    a, b, c ‚Äì –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç—ã –∫–≤–∞–¥—Ä–∞—Ç–Ω–æ–≥–æ —É—Ä–∞–≤–Ω–µ–Ω–∏—è ùëéùë•**2+ùëèùë•+ùëê=0 –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –µ–≥–æ –∫–æ—Ä–Ω–∏ –≤ –ø–æ—Ä—è–¥–∫–µ –≤–æ–∑—Ä–∞—Å—Ç–∞–Ω–∏—è.

    –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –° –ø–æ–¥–æ–±–Ω–æ–π –∑–∞–¥–∞—á–µ–π –º—ã —É–∂–µ —Å—Ç–∞–ª–∫–∏–≤–∞–ª–∏—Å—å.

    –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 2. –ì–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç—Å—è, —á—Ç–æ –∫–≤–∞–¥—Ä–∞—Ç–Ω–æ–µ —É—Ä–∞–≤–Ω–µ–Ω–∏–µ –∏–º–µ–µ—Ç —Ö–æ—Ç—è –±—ã –æ–¥–∏–Ω –∫–æ—Ä–µ–Ω—å.

    –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 3. –°–ª–µ–¥—É—é—â–∏–π –ø—Ä–æ–≥—Ä–∞–º–º–Ω—ã–π –∫–æ–¥:
    print(solve(1, -4, -5))
    print(solve(-2, 7, -5))
    print(solve(1, 2, 1))

    –¥–æ–ª–∂–µ–Ω –≤—ã–≤–æ–¥–∏—Ç—å:
    -1.0 5.0
    1.0 2.5
    -1.0 -1.0

    –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ï—Å–ª–∏ —É—Ä–∞–≤–Ω–µ–Ω–∏–µ –∏–º–µ–µ—Ç —Ç–æ–ª—å–∫–æ –æ–¥–∏–Ω –∫–æ—Ä–µ–Ω—å, –Ω—É–∂–Ω–æ –≤–µ—Ä–Ω—É—Ç—å –¥–≤–∞ —á–∏—Å–ª–∞, —Ä–∞–≤–Ω—ã—Ö —ç—Ç–æ–º—É –∫–æ—Ä–Ω—é.
"""
def solve(a, b, c):
    d = b**2-4*a*c
#    if  d<0 : print("–ù–µ—Ç –∫–æ—Ä–Ω–µ–π")
    if d==0 : return -b/2/a , -b/2/a 
    elif d>0 : return min((-b-d**0.5)/2/a, (-b+d**0.5)/2/a), max((-b-d**0.5)/2/a, (-b+d**0.5)/2/a)

# —Å—á–∏—Ç—ã–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ
a, b, c = int(input()), int(input()), int(input())

# –≤—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é
x1, x2 = solve(a, b, c)
print(x1, x2)