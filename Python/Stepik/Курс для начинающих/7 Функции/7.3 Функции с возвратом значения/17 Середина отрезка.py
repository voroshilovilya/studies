"""
    –ù–∞–ø–∏—à–∏—Ç–µ —Ñ—É–Ω–∫—Ü–∏—é get_middle_point(x1, y1, x2, y2), –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –≤ –∫–∞—á–µ—Å—Ç–≤–µ 
    –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã –∫–æ–Ω—Ü–æ–≤ –æ—Ç—Ä–µ–∑–∫–∞ (ùë•1;ùë¶1) –∏ (ùë•2;ùë¶2) –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã 
    —Ç–æ—á–∫–∏ —è–≤–ª—è—é—â–µ–π—Å—è —Å–µ—Ä–µ–¥–∏–Ω–æ–π –¥–∞–Ω–Ω–æ–≥–æ –æ—Ç—Ä–µ–∑–∫–∞.

    –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –ö–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã —Å–µ—Ä–µ–¥–∏–Ω—ã –æ—Ç—Ä–µ–∑–∫–∞ —Å –∫–æ–Ω—Ü–∞–º–∏ –≤ —Ç–æ—á–∫–∞—Ö (ùë•1;ùë¶1) –∏ (ùë•2;ùë¶2)
    –≤—ã—á–∏—Å–ª—è—é—Ç—Å—è –ø–æ —Ñ–æ—Ä–º—É–ª–µ:
    ((ùë•1+ùë•2)/2;(ùë¶1+ùë¶2)/2)

    –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 2. –°–ª–µ–¥—É—é—â–∏–π –ø—Ä–æ–≥—Ä–∞–º–º–Ω—ã–π –∫–æ–¥:
    print(get_middle_point(0, 0, 10, 0))
    print(get_middle_point(1, 5, 8, 3))

    –¥–æ–ª–∂–µ–Ω –≤—ã–≤–æ–¥–∏—Ç—å:
    5.0 0.0
    4.5 4.0

    Sample Input:
    1
    1
    2
    2
    Sample Output:
    1.5 1.5
"""
def get_middle_point(x1, y1, x2, y2):
    return (x1+x2)/2, (y1+y2)/2
    

# —Å—á–∏—Ç—ã–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ
x_1, y_1 = int(input()), int(input())
x_2, y_2 = int(input()), int(input())

# –≤—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é
x, y = get_middle_point(x_1, y_1, x_2, y_2)
print(x, y)