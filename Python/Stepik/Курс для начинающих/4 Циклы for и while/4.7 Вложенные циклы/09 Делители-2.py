"""
    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ ùëõ. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –≤—ã–≤–æ–¥—è—â—É—é –≥—Ä–∞—Ñ–∏—á–µ—Å–∫–æ–µ 
    –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –¥–µ–ª–∏–º–æ—Å—Ç–∏ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ ùëõ –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ. –í –∫–∞–∂–¥–æ–π —Å—Ç—Ä–æ–∫–µ –Ω–∞–¥–æ –Ω–∞–ø–µ—á–∞—Ç–∞—Ç—å 
    –æ—á–µ—Ä–µ–¥–Ω–æ–µ —á–∏—Å–ª–æ –∏ —Å—Ç–æ–ª—å–∫–æ —Å–∏–º–≤–æ–ª–æ–≤ ¬´+¬ª, —Å–∫–æ–ª—å–∫–æ –¥–µ–ª–∏—Ç–µ–ª–µ–π —É —ç—Ç–æ–≥–æ —á–∏—Å–ª–∞.

    –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –æ–¥–Ω–æ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ.

    –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –≥—Ä–∞—Ñ–∏—á–µ—Å–∫–æ–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ ùëõ, –∫–∞–∂–¥–æ–µ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.

    Sample Input:
    5
    Sample Output:
    1+
    2++
    3++
    4+++
    5++
"""
a = int(input())

c = 0
d = 0
k = 0
for i in range (1, a+1):
    for j in range (1,i+1):
        if i%j==0 : 
            c+=1
            k, d = c, j
    c=0
    print (d,k*"+", sep="")