"""
    Вводятся 3 строки в случайном порядке. Напишите программу, которая выясняет, 
    можно ли из длин этих строк построить арифметическую прогрессию.

    Формат входных данных
    На вход программе подаются три строки, каждая на отдельной строке.

    Формат выходных данных
    Программа должна вывести строку «YES», если из длин введенных слов можно 
    построить арифметическую прогрессию, или «NO» - в противном случае.

    Примечание. Почитать про арифметическую прогрессию можно по ссылке.

    Sample Input 1:
    abc
    a
    abcde
    Sample Output 1:
    YES
"""
a1, b1, c1= len(input()), len(input()), len(input())

if (2*b1-c1-a1)*(2*c1-b1-a1)*(2*a1-b1-c1) == 0 : print("YES")
else : print ("NO")