"""
    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö —á–∏—Å–ª–∞ ùëõ –∏ ùëö. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è —Å–æ–∑–¥–∞–µ—Ç –º–∞—Ç—Ä–∏—Ü—É 
    —Ä–∞–∑–º–µ—Ä–æ–º ùëõ√óùëö, –∑–∞–ø–æ–ª–Ω–∏–≤ –µ—ë "—Å–ø–∏—Ä–∞–ª—å—é" –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å –æ–±—Ä–∞–∑—Ü–æ–º.

    –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
    –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –Ω–∞ –æ–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö —á–∏—Å–ª–∞ ùëõ –∏ ùëö ‚Äî –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤ 
    –≤ –º–∞—Ç—Ä–∏—Ü–µ.

    –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
    –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –º–∞—Ç—Ä–∏—Ü—É –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ –æ–±—Ä–∞–∑—Ü–æ–º.

    –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –î–ª—è –≤—ã–≤–æ–¥–∞ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –º–∞—Ç—Ä–∏—Ü—ã –∫–∞–∫ –≤ –ø—Ä–∏–º–µ—Ä–∞—Ö –æ—Ç–≤–æ–¥–∏—Ç–µ —Ä–æ–≤–Ω–æ 3 —Å–∏–º–≤–æ–ª–∞ –Ω–∞ –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç. 
    –î–ª—è —ç—Ç–æ–≥–æ –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ —Å—Ç—Ä–æ–∫–æ–≤—ã–π –º–µ—Ç–æ–¥ ljust(). –ú–æ–∂–Ω–æ –æ–±–æ–π—Ç–∏—Å—å –∏ –±–µ–∑ ljust(), —Å–∏—Å—Ç–µ–º–∞ –ø—Ä–∏–º–µ—Ç –∏ —Ç–∞–∫–æ–µ —Ä–µ—à–µ–Ω–∏–µ 

    Sample Input 1:
    4 5
    Sample Output 1:
    1  2  3  4  5
    14 15 16 17 6
    13 20 19 18 7
    12 11 10 9  8
"""
r, c  = [ int(i) for i in input().split()]
x, s = r, c
a = [[0 for i in range(c)] for j in range(r)] 
count = 0 
qwe = r*c
for i in range(c):   
    count += 1
    a[0][i] = count
j = 0   # —É–∫–∞–∑—ã–≤–∞–µ–º –ø–æ—Å–ª–µ–¥–Ω—é—é –∑–∞–ø–æ–ª–Ω–µ–Ω–Ω—É—é —è—á–µ–π–∫—É
i = 0
i = c-1
r -= 1
c -= 1
while qwe != count:  
    for k in range(r):  #–¥–≤–∏–∂–µ–Ω–∏–µ –≤–Ω–∏–∑
        j += 1
        count += 1
        a[j][i] = count        
    if qwe == count:
        break
    for k in range(c):  #–¥–≤–∏–∂–µ–Ω–∏–µ –≤–ª–µ–≤–æ
        i -= 1
        count += 1 
        a[j][i] = count
    if qwe == count:
        break
    r -= 1    
    for k in range(r):  #–¥–≤–∏–∂–µ–Ω–∏–µ –≤–≤–µ—Ä—Ö
        j -= 1
        count += 1
        a[j][i] = count
    if qwe == count:
        break
    c -= 1   
    for k in range(c): #–¥–≤–∏–∂–µ–Ω–∏–µ –≤–ø—Ä–∞–≤–æ
        i += 1
        count += 1
        a[j][i] = count
    r -= 1
    c -= 1

for i in range(x):
    for j in range(s):
        print(str(a[i][j]).ljust(3), end=' ')
    print()