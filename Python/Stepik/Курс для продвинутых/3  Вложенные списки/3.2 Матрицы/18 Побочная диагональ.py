"""
    На вход программе подается натуральное число 𝑛. Напишите программу, которая создает матрицу 
    размером 𝑛×𝑛 и заполняет её по следующему правилу:
    числа на побочной диагонали равны 1;
    числа, стоящие выше этой диагонали, равны 0;
    числа, стоящие ниже этой диагонали, равны 2.

    Полученную матрицу выведите на экран. Числа в строке разделяйте одним пробелом.

    Формат входных данных
    На вход программе подается натуральное число 𝑛 — количество строк и столбцов в матрице.

    Формат выходных данных
    Программа должна вывести матрицу в соответствии с условием задачи.

    Примечание. Побочная диагональ — это диагональ, идущая из правого верхнего в левый нижний угол матрицы.

    Sample Input 1:
    4
    Sample Output 1:
    0 0 0 1
    0 0 1 2
    0 1 2 2
    1 2 2 2
"""
x = int(input())
n = [ [ i for i in range (x) ] for j in range (x) ]
for i in range (x):
    for j in range (x):
        if (i > j and i < x - 1 - j) or ( i <= j and i < x - 1 - j ):
            n[i][j] = 0
        if (i <= j and i > x - 1 - j) or ( i > j and i > x - 1 - j ):
            n[i][j] = 2
        if (i == x - 1 - j):
            n[i][j] = 1
            
[print (*i) for i in n ]