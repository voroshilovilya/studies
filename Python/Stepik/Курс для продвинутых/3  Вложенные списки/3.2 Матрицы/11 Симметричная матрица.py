x = int(input())
n = [ input().split() for _ in range (x)]
#[print(*i) for i in n]
count = 0
for i in range(x):
    for j in range(x):
        if n[i][j] == n[j][i]:count+=1
if count == x**2 : print('YES')
else : print('NO')