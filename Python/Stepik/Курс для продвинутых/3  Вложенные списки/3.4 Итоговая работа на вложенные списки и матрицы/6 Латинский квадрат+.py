n = int(input())
a = [[ int(i) for i in input().split()] for j in range(n)]
b = 1
for i in range(n):
    row = []
    col = []
    for j in range(n):
        if 0<a[i][j]<n+1 and a[i][j] not in (row):
            row.append(a[i][j])
        if 0<a[j][i]<n+1 and a[j][i] not in (col):
            col.append(a[j][i])
    if  len(row)<n  or  len(col)<n : 
        b = 0
        break

if b == 1: print('YES')
else : print('NO')