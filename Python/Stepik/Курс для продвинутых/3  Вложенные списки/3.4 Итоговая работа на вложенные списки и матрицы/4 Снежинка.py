n = int(input())
a = [['.' for i in range (n)]for j in range (n)]

for i in range(n):
    for j in range(n):
        if i + j + 1 == n : a[i][j] = '*'
        if i==j : a[i][j] = '*'
        if i == n//2: a[i][j] = '*'
        if j == n//2: a[i][j] = '*'
        
        
[print(*i) for i in a]