def stepen (mat1, mat2, x):
    c = [[0] * x for _ in range(x)]
    for i in range(x):
        for j in range(x):
            for q in range(x):
                c[i][j] += mat1[i][q] * mat2[q][j]
    return c                                       


n = int(input())
a = [[int(i) for i in input().split()] for _ in range(n)]
st = int(input())

b = a.copy()

for i in range(st-1):
    b = stepen (a,b,n)
    
[print(*i) for i in b]