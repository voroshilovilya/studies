n, m = [int(i) for i in input().split()]
a = [[int(i) for i in input().split()] for _ in range(n)]
input()
m, k = [int(i) for i in input().split()]
b = [[int(i) for i in input().split()] for _ in range(m)]
c = [[0] * k for _ in range(n)]

